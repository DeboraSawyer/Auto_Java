public class Pessoa {
    private String nome; // private: somente a classe Pessoa tem acesso ao atributo nome.
    private int idade;

    public Pessoa(String nome, int idade) {
        this.nome = nome;
        this.idade = idade;
    }

    public String getNome() {
        return nome;
    }

    public int getIdade() {
        return idade;
    }
}